package main

import "fmt"

func main(){


	fmt.Printf("Hello %T %v", 10, 20 )
	fmt.Println()
	fmt.Printf("Number: %b", 1234)
	fmt.Println()
	fmt.Printf("Number: %o", 10008)
	fmt.Println()
	fmt.Printf("Number: %d", 111001)
	fmt.Println()
	fmt.Printf("Number: %e", 11.100145545)
	fmt.Println()
	fmt.Printf("Number: %f", 5.134541001)
	fmt.Println()
	fmt.Printf("Number: %g", 112435611001)




}